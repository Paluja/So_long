#include "libft.h"

int	ft_atoi(const char *nptr)
{
	unsigned long long	result;
	int					i;
	int					negative;

	result = 0;
	i = 0;
	negative = 0;
	while (nptr[i] == ' ' || nptr[i] == '\f' || nptr[i] == '\n'
		|| nptr[i] == '\r' || nptr[i] == '\t' || nptr[i] == '\v')
		i++;
	if (nptr[i] == '-')
		negative = 1;
	if (nptr[i] == '-' || nptr[i] == '+')
		i++;
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		result *= 10;
		result += nptr[i] - '0';
		i++;
	}
	if (negative)
	{
		result = result * -1;
	}
	return (result);
}
